#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	dstlen;
	size_t	srclen;

	dstlen = ft_strlen(dst);
	i = dstlen;
	j = 0;
	if (i < size - 1 && size > 0)
	{
		while (src[j] && i < size - 1)
		{
			dst[i] = src[j];
			i++;
			j++;
		}
		dst[i] = '\0';
	}
	if (dstlen >= size)
		dstlen = size;
	srclen = ft_strlen(src);
	return (dstlen + srclen);
}
